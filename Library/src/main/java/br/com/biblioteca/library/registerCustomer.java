    private void confirmJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmJBActionPerformed
        String name = nameJTF.getText();
        String cpf = cpfJTF.getText();
        String adress = adressJTF.getText();
        String email = emailJTF.getText();
        
        if(name.isEmpty() || cpf.isEmpty() || adress.isEmpty() || email.isEmpty()){
            JOptionPane.showMessageDialog(this, "Pleas, fill in the blank fields");
            return;
        }
        
        ModelCustomers Client = new ModelCustomers(name, cpf, adress, email);
        Customers.add(Client);
        
         JOptionPane.showMessageDialog(this, "Register Complete");
        this.dispose();
    }//GEN-LAST:event_confirmJBActionPerformed

